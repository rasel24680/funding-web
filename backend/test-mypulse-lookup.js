async function getToken() {
  console.log("\nв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
  console.log("STEP 1: Getting OAuth Token");
  console.log("в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђ");
  console.log(`URL: ${TOKEN_URL}`);
  console.log(`Client ID: ${CLIENT_ID}`);
  console.log(`Scope: ${SCOPE}`);

  const params = new URLSearchParams({
    grant_type: "client_credentials",
    client_id: CLIENT_ID,
    client_secret: CLIENT_SECRET,
    scope: SCOPE,
  });

  const response = await fetch(TOKEN_URL, {
    method: "POST",
    headers: { "Content-Type": "application/x-www-form-urlencoded" },
    body: params.toString(),
  });

  const data = await response.json();

  if (!response.ok) {
    console.log(`\nвќЊ Token FAILED - Status: ${response.status}`);
    console.log(JSON.stringify(data, null, 2));
    throw new Error(
      `Token failed: ${data.error_description || data.error || response.status}`,
    );
  }

  console.log(`\nвњ… Token OK - expires in ${data.expires_in}s`);
  return data.access_token;
}

